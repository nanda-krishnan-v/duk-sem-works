# Integer (int) examples
x = 10
y = -5
z = 0

print("Integer examples:")
print("x =", x)
print("y =", y)
print("z =", z)

# Float (float) examples
a = 10.5
b = -2.75
c = 1.2e3

print("\nFloat examples:")
print("a =", a)
print("b =", b)
print("c =", c)

# Complex (complex) examples
p = 3 + 4j
q = -2 - 5j

print("\nComplex number examples:")
print("p =", p)
print("q =", q)

# Performing operations on numeric types
int_sum = x + y
print("\nSum of integers (x + y):", int_sum)

float_mult = a * b
print("Multiplication of floats (a * b):", float_mult)

complex_add = p + q
print("Addition of complex numbers (p + q):", complex_add)
